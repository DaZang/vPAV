package de.viadee.bpm.vPAV.processing.code.flow;

public class Node {

    private InSet in;
    private OutSet out;
    private Node from;
    private Node to;

    public Node(final Node from, final Node to) {
        this.from = from;
        this.to = to;
    }

    public InSet getIn() {
        return in;
    }

    public void setIn(InSet in) {
        this.in = in;
    }

    public OutSet getOut() {
        return out;
    }

    public void setOut(OutSet out) {
        this.out = out;
    }

    public Node getFrom() {
        return from;
    }

    public void setFrom(Node from) {
        this.from = from;
    }

    public Node getTo() {
        return to;
    }

    public void setTo(Node to) {
        this.to = to;
    }

}
